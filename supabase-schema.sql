create table if not exists public.saris (
  id bigint generated by default as identity primary key,
  name text not null default '',
  fabric text not null,
  region text not null,
  tags text[] not null default '{}',
  price_min integer not null check (price_min >= 0),
  price_max integer not null check (price_max >= price_min),
  quantity integer not null default 0 check (quantity >= 0),
  reserved boolean not null default false,
  image_data text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.transactions (
  id bigint generated by default as identity primary key,
  sari_id bigint references public.saris (id) on delete set null,
  sari_name text not null,
  buyer text not null,
  qty integer not null check (qty > 0),
  actual_price numeric(10,2) not null check (actual_price > 0),
  method text not null check (method in ('Cash', 'Venmo', 'Zelle', 'UPI')),
  sale_date date not null,
  created_at timestamptz not null default now()
);

alter table public.saris
  add column if not exists tags text[] not null default '{}';

alter table public.saris
  alter column name set default '';

create index if not exists idx_transactions_sari_id on public.transactions (sari_id);
create index if not exists idx_transactions_sale_date on public.transactions (sale_date desc);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_saris_updated_at on public.saris;
create trigger trg_saris_updated_at
before update on public.saris
for each row
execute function public.set_updated_at();

create or replace function public.apply_transaction_inventory_delta()
returns trigger
language plpgsql
as $$
declare
  new_qty integer;
begin
  if tg_op = 'INSERT' then
    if new.sari_id is not null then
      update public.saris
      set quantity = quantity - new.qty,
          reserved = case when quantity - new.qty <= 0 then false else reserved end
      where id = new.sari_id
      returning quantity into new_qty;

      if new_qty is null or new_qty < 0 then
        raise exception 'Not enough inventory for sari_id %', new.sari_id;
      end if;
    end if;
    return new;
  end if;

  if tg_op = 'DELETE' then
    if old.sari_id is not null then
      update public.saris
      set quantity = quantity + old.qty
      where id = old.sari_id;
    end if;
    return old;
  end if;

  if tg_op = 'UPDATE' then
    if old.sari_id = new.sari_id then
      if new.sari_id is not null then
        update public.saris
        set quantity = quantity + old.qty - new.qty,
            reserved = case when quantity + old.qty - new.qty <= 0 then false else reserved end
        where id = new.sari_id
        returning quantity into new_qty;

        if new_qty is null or new_qty < 0 then
          raise exception 'Not enough inventory for sari_id %', new.sari_id;
        end if;
      end if;
    else
      if old.sari_id is not null then
        update public.saris
        set quantity = quantity + old.qty
        where id = old.sari_id;
      end if;

      if new.sari_id is not null then
        update public.saris
        set quantity = quantity - new.qty,
            reserved = case when quantity - new.qty <= 0 then false else reserved end
        where id = new.sari_id
        returning quantity into new_qty;

        if new_qty is null or new_qty < 0 then
          raise exception 'Not enough inventory for sari_id %', new.sari_id;
        end if;
      end if;
    end if;
    return new;
  end if;

  return null;
end;
$$;

drop trigger if exists trg_transactions_inventory_delta on public.transactions;
create trigger trg_transactions_inventory_delta
before insert or update or delete on public.transactions
for each row
execute function public.apply_transaction_inventory_delta();

alter table public.saris enable row level security;
