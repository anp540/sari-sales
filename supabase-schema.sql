drop policy if exists "Allow full access on saris" on public.saris;
create policy "Allow full access on saris"
on public.saris
for all
to anon, authenticated
using (true)
with check (true);

drop policy if exists "Allow full access on transactions" on public.transactions;
create policy "Allow full access on transactions"
on public.transactions
for all
to anon, authenticated
using (true)
with check (true);

do $$
begin
  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'saris'
  ) then
    alter publication supabase_realtime add table public.saris;
  end if;

  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'transactions'
  ) then
    alter publication supabase_realtime add table public.transactions;
  end if;
end $$;

